void SysTick_Handler(void)
{
  SYS_TIME++;
  PT *p = THREADS_HEAD;
  while(p){
    if(p->count >0){
      p->count -=1;
    }
    else{
      p->ready = 1;
      p->count = p->load-1;
    }
    p = p->next;
  }
}
